export function translate(lang: Language, key: string, params?: Record<string, string>): string {
  const raw = STRINGS[lang]?.[key] ?? STRINGS.en[key] ?? key;
  if (!params) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? `{${k}}`);
}
